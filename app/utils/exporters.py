class ReportExporter:
    """
    Multi-format report exporter for SOC findings
    Supports: PDF, HTML, JSON, CSV, Markdown, Excel
    """
    
    def __init__(self, company_name: str = "Security Operations Center"):
        self.company_name = company_name
        self.export_dir = REPORTS_DIR / f"exports_{datetime.now().strftime('%Y%m%d')}"
        self.export_dir.mkdir(parents=True, exist_ok=True)
        
    def export_json(self, data: Dict[str, Any], filename: str = None) -> str:
        """
        Export data to JSON format
        
        Args:
            data: Dictionary data to export
            filename: Optional filename (auto-generated if not provided)
            
        Returns:
            Path to exported file
        """
        if filename is None:
            filename = f"report_{datetime.now().strftime('%Y%m%d_%H%M%S')}.json"
        
        filepath = self.export_dir / filename
        
        with open(filepath, 'w') as f:
            json.dump(data, f, indent=2, default=str)
        
        return str(filepath)
    
    def export_csv(self, df: pd.DataFrame, filename: str = None) -> str:
        """
        Export DataFrame to CSV
        
        Args:
            df: Pandas DataFrame
            filename: Optional filename
            
        Returns:
            Path to exported file
        """
        if filename is None:
            filename = f"data_{datetime.now().strftime('%Y%m%d_%H%M%S')}.csv"
        
        filepath = self.export_dir / filename
        df.to_csv(filepath, index=False)
        
        return str(filepath)
    
    def export_markdown(self, report: Dict[str, Any], filename: str = None) -> str:
        """
        Export report to Markdown format
        
        Args:
            report: Report dictionary
            filename: Optional filename
            
        Returns:
            Path to exported file
        """
        if filename is None:
            filename = f"report_{datetime.now().strftime('%Y%m%d_%H%M%S')}.md"
        
        md_content = self._generate_markdown(report)
        
        filepath = self.export_dir / filename
        with open(filepath, 'w', encoding='utf-8') as f:
            f.write(md_content)
        
        return str(filepath)
    
    def _generate_markdown(self, report: Dict[str, Any]) -> str:
        """Generate Markdown content from report"""
        md = f"""# {APP_NAME} - Security Incident Report

## Report Information
- **Report ID:** {report.get('report_metadata', {}).get('report_id', 'N/A')}
- **Generated By:** {APP_NAME} v{APP_VERSION}
- **Date:** {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}
- **Classification:** CONFIDENTIAL

---

## Executive Summary

### Risk Assessment
- **Risk Score:** {report.get('executive_summary', {}).get('risk_score', 'N/A')}/100
- **Risk Level:** {report.get('executive_summary', {}).get('risk_level', 'N/A')}
- **Total Events Analyzed:** {report.get('executive_summary', {}).get('total_events_analyzed', 0)}

### Key Findings
"""
        for finding in report.get('executive_summary', {}).get('key_findings', []):
            md += f"- {finding}\n"
        
        md += f"""
---

## Technical Findings

| Finding Type | Count | Severity |
|-------------|-------|----------|
"""
        tech_table = report.get('technical_findings', {}).get('summary_table', {})
        for key, value in tech_table.items():
            md += f"| {key.replace('_', ' ').title()} | {value} | - |\n"
        
        md += f"""
---

## Indicators of Compromise

### IP Addresses
"""
        for ip in report.get('indicators_of_compromise', {}).get('ip_addresses', [])[:10]:
            md += f"- `{ip.get('indicator', 'N/A')}`\n"
        
        md += f"""
---

## Recommendations

"""
        for i, rec in enumerate(report.get('recommendations', [])[:5], 1):
            md += f"{i}. {rec}\n"
        
        md += f"""
---

*Report generated by {APP_NAME} v{APP_VERSION} - Confidential SOC Document*
"""
        return md
    
    def export_html(self, report: Dict[str, Any], findings: Dict[str, Any] = None,
                   charts: Dict[str, str] = None, filename: str = None) -> str:
        """
        Export report to HTML format with optional charts
        
        Args:
            report: Report dictionary
            findings: Raw findings for detailed tables
            charts: Dictionary of base64-encoded charts
            filename: Optional filename
            
        Returns:
            Path to exported file
        """
        if filename is None:
            filename = f"report_{datetime.now().strftime('%Y%m%d_%H%M%S')}.html"
        
        html_content = self._generate_html(report, findings, charts)
        
        filepath = self.export_dir / filename
        with open(filepath, 'w', encoding='utf-8') as f:
            f.write(html_content)
        
        return str(filepath)
    
