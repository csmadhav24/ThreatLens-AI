def quick_export(data: Union[Dict, pd.DataFrame], format: str = 'json', 
                filename: str = None) -> str:
    """Quick export helper"""
    exporter = ReportExporter()
    
    if format == 'json' and isinstance(data, dict):
        return exporter.export_json(data, filename)
    elif format == 'csv' and isinstance(data, pd.DataFrame):
        return exporter.export_csv(data, filename)
    elif format == 'markdown' and isinstance(data, dict):
        return exporter.export_markdown(data, filename)
    else:
        raise ValueError(f"Unsupported format {format} for data type {type(data)}")


